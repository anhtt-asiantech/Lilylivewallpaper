package com.anhttvn.roselivewallpaper.model;

public class SliderItem {
    public String image;
    public int position;

    public void setImage(String img) {
        this.image = img;
    }

    public String getImage() {
        return image;
    }

    public void setPosition(int pos) {
        this.position = pos;
    }

    public int getPosition() {
        return position;
    }
}
